class UserProfile {
  final String id;
  final String name;
  final String address;
  final String email;
  final String contactNumber;
  final String profilePicUrl;

  UserProfile({
    required this.id,
    required this.name,
    required this.address,
    required this.email,
    required this.contactNumber,
    required this.profilePicUrl,
  });
}
